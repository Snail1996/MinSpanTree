%-------------------------------------------------------------------------------
%%构建江苏省市级网络，节点为城市，边为现有路网可达性。边权重为距离（单位公里），
%%利用最小权重生成树方法，设计江苏省最优高铁设计图。
%%注意输入为‘Prim’或者‘Kruskal'，其他参数输入无效
%-------------------------------------------------------------------------------
%1――南京，2――徐州，3――扬州，4――镇江，5――常州，6――无锡
%7――苏州，8――宿迁，9――淮安，10――泰州，11――南通，12――连云港，13――盐城
%-------------------------------------------------------------------------------
%生成城市节点邻接矩阵
A = zeros(13);
A(1,[2 3 4 5]) = [327.2 98.8 84.4 128.7];
A(2,[8 12]) = [112.3 205.5];
A(3, [4 9 10]) = [36.0 160.5 72.6];
A(4, 5) = 80.4;
A(5, 6) = 62.8;
A(6, [7 10]) = [42.1 131.4];
A(7, 11) = 103.9;
A(8, 9) = 109.3;
A(9, [12 13]) = [128.9 116.7];
A(10, [11 13]) = [125.8 123.7];
A(11, 13) = 187.3;
A(12, 13) = 193.7;
G = A+A';
%-------------------------------------------------------------------------------
ALGORITHM  = input('若运行Prim算法，则输入"Prim";若运行Kruskal算法，则输入"Kruskal":\n');
switch(ALGORITHM)
    case 'Prim'
        %Prim算法
        A = Prim(13, G)
        
    case 'Kruskal'
        %Kruskal算法
        A = Kruskal(13, G)
    otherwise
        fprintf('输入参数有误！');
end


